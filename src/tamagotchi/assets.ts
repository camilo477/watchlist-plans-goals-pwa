export type ActionType = 0 | 1 | 2 | 3 | 4;
export const ACT_MUSIC: ActionType = 0;
export const ACT_SLEEP: ActionType = 1;
export const ACT_EAT: ActionType = 2;
export const ACT_BATH: ActionType = 3;
export const ACT_SICK: ActionType = 4;

export function getFrameFor(action: ActionType, frame: number) {
  const f = frame & 3;
  switch (action) {
    case ACT_MUSIC: return FR_MUSIC[f];
    case ACT_SLEEP: return FR_SLEEP[f];
    case ACT_EAT:   return FR_EAT[f];
    case ACT_BATH:  return FR_BATH[f];
    case ACT_SICK:  return FR_SICK[f];
    default:        return FR_IDLE[f];
  }
}
